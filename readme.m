This Matlab package reproduces the figure of the article 

Adaptive estimation of the density matrix in quantum homodyne tomography with noisy data}
P. Alquier, K.Meziani, G.Peyré

The main scripts are
- test_patterns.m : display the pattern functions	
- test_rmse.m : display the evolution of the RMSE as a function of n the number of sample.	
- test_stat_q.m : display a 3-D plot of the recovered density matrix for a given value of n.
